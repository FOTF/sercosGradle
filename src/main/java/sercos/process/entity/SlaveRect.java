public class SlaveRect {

    private int x;

    private int y;

    private int width;

    private int height;

    private int ovalX;

    private int ovalY;

    private int ovalWidth;

    private int ovalHeight;

    public int getOvalX() {
        return ovalX;
    }

    public void setOvalX(int ovalX) {
        this.ovalX = ovalX;
    }

    public int getOvalY() {
        return ovalY;
    }

    public void setOvalY(int ovalY) {
        this.ovalY = ovalY;
    }

    public int getOvalWidth() {
        return ovalWidth;
    }

    public void setOvalWidth(int ovalWidth) {
        this.ovalWidth = ovalWidth;
    }

    public int getOvalHeight() {
        return ovalHeight;
    }

    public void setOvalHeight(int ovalHeight) {
        this.ovalHeight = ovalHeight;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
